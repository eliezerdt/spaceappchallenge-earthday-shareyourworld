            <h1>Net Radiation</h1>
              <p>Earth's net radiation, sometimes called net flux, is the balance between incoming and outgoing energy at the top of the atmosphere. It is the total energy that is available to influence the climate. Energy comes in to the system when sunlight penetrates the top of the atmosphere. Energy goes out in two ways: reflection by clouds, aerosols, or the Earth's surface; and thermal radiation—heat emitted by the surface and the atmosphere, including clouds. The global average net radiation must be close to zero over the span of a year or else the average temperature will rise or fall.
              <p>These maps show monthly net radiation in watts per square meter. Places where the amounts of incoming and outgoing energy were in balance are yellow. Places where more energy was coming in than going out (positive net radiation) are red. Places where more energy was going out than coming in (negative net radiation) are blue-green. The measurements were made by the Clouds and the Earth's Radiant Energy System <a href="http://aqua.nasa.gov/about/instrument_ceres.php">(CERES)</a> sensors on NASA&rsquo;s Terra and Aqua satellites.</p>
                <p>Over the course of a year, the most obvious pattern is seasonal changes in net radiation. Incoming sunlight increases in the hemisphere experiencing summer, which makes the energy imbalance strongly positive (more watts of energy coming in than going out). As the September equinox approaches, a zone of positive net radiation is nearly centered over the equator, and energy deficits lie over the poles. As the season changes into winter, the net radiation becomes negative across much of the Northern Hemisphere and positive in the Southern Hemisphere. The pattern reverses on the March equinox.</p>
                <p>Averaged over the year, there is a net energy surplus at the equator and a net energy deficit at the poles. This equator-versus-pole energy imbalance is the fundamental driver of atmospheric and oceanic circulation.</p>
